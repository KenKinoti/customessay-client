                         Once you’ve collected all the relevant preparation information, you’ll need to draft your case analysis. Since a case study involves empirical investigation into a phenomenon, event, institution, policy, or people in a real-life context, you may need to conduct experiments depending on the type and nature of your research. That said, the draft you prepare for your analysis should have the following sections.</p>

                         <h4>Introduction</h4>
                         <p>The introduction section is where you identify the critical problems in your case study. Thus, your introduction must include your thesis statement. Some sponsors/academicians allow for the thesis statement to be included separately as a standalone text. In such cases, it should be brief—two to three sentences—that summarizes the outcome of your analysis.</p>

                         <h4>Background</h4>
                         <p>The background information is what sets the stage for an excellent case study. Any facts, statistics, and experiments you’ve conducted should be outlined here. The background information showcases how well you’ve researched for your case study.</p>

                         <h4>Proposed Solution</h4>
                         <p>A case study is an investigative endeavor that seeks to find a solution to an existing problem. Thus, a good case study should include a proposed solution that the author deems most appropriate for the task. Explain in detail why the solution proved to be the best and back it up with solid evidence.</p>

                         <h4>Alternatives</h4>
                         <p>A good draft should have an alternative section. This is where you outline all the alternative remedies to the problem in question. Include all the viable options and explain why they were rejected or why they are currently not the best solution to the issue at hand.</p>

                         <h4>Recommendations</h4>
